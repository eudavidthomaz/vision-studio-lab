-- Corrigir search_path mutável nas 3 funções de trigger

-- 1. Corrigir update_content_library_updated_at
CREATE OR REPLACE FUNCTION public.update_content_library_updated_at()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$function$;

